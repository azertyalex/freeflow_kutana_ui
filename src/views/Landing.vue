<template>
    <section class="landing">
        <v-row class="title mx-0">
            <v-col align="center">
                <h1 class="ttl">FreeFlowConnect</h1>
            </v-col>
        </v-row>
        <v-row class="io mb-2" justify="center" align="center">
            <v-btn-toggle rounded class="primary mr-1" dark>
                <v-tooltip bottom>
                    <template v-slot:activator="{ on: tooltip }">
                        <v-btn class="primary" @click="toggleCam" v-on="{ ...tooltip}">
                            <v-icon>{{ video ? 'videocam' : 'videocam_off' }}</v-icon>
                        </v-btn>
                    </template>
                    <span>{{ video ? 'Deactivate camera' : 'Activate camera' }}</span>
                </v-tooltip>
                <v-menu top left offset-y v-if="video">
                    <template v-slot:activator="{ on: menu }">
                        <v-tooltip bottom>
                            <template v-slot:activator="{ on: tooltip }">
                                <v-btn class="small" v-on="{ ...tooltip, ...menu }">
                                    <v-icon small>expand_less</v-icon>
                                </v-btn>
                            </template>
                            <span>Change video input</span>
                        </v-tooltip>
                    </template>
                    <v-list>
                        <v-list-item-group
                            :value="videoInputDevices.map(d=>d.deviceId).indexOf(videoDevice)"
                            color="primary"
                        >
                            <v-list-item
                                v-for="(item) in videoInputDevices"
                                :key="item.deviceId"
                                @click="changeVideoTo(item.deviceId)"
                            >
                                <v-list-item-title>{{ item.label }}</v-list-item-title>
                            </v-list-item>
                        </v-list-item-group>
                    </v-list>
                </v-menu>
            </v-btn-toggle>

            <v-btn-toggle multiple rounded class="primary mr-1" dark>
                <v-tooltip bottom>
                    <template v-slot:activator="{ on: tooltip }">
                        <v-btn class="primary" @click="toggleMic" v-on="{ ...tooltip}">
                            <v-icon>{{ audio ? 'mic' : 'mic_off' }}</v-icon>
                        </v-btn>
                    </template>
                    <span>{{ audio ? 'Unmute' : 'Mute' }}</span>
                </v-tooltip>
                <v-menu top left offset-y v-if="audio">
                    <template v-slot:activator="{ on: menu }">
                        <v-tooltip bottom>
                            <template v-slot:activator="{ on: tooltip }">
                                <v-btn class="small" v-on="{ ...tooltip, ...menu }">
                                    <v-icon small>expand_less</v-icon>
                                </v-btn>
                            </template>
                            <span>Change audio input</span>
                        </v-tooltip>
                    </template>
                    <v-list>
                        <v-list-item-group
                            :value="audioInputDevices.map(d=>d.deviceId).indexOf(audioDevice)"
                            color="primary"
                        >
                            <v-list-item
                                v-for="(item) in audioInputDevices"
                                :key="item.deviceId"
                                @click="changeAudioInputTo(item.deviceId)"
                            >
                                <v-list-item-title>{{ item.label }}</v-list-item-title>
                            </v-list-item>
                        </v-list-item-group>
                    </v-list>
                </v-menu>
            </v-btn-toggle>
        </v-row>
        <v-row class="actions pa-2" justify="center" align="center">
            <v-col cols="4">
                <v-form @submit.prevent="joinRoom" v-model="valid">
                    <v-text-field
                        :rules="inviteUrlRules"
                        filled
                        hint="Paste the room ID or link you've received"
                        id="roomId"
                        label="Room ID"
                        hide-details
                        required
                        v-model="inviteUrl"
                        background-color="#a1a1a1c9"
                    >
                        <template v-slot:append>
                            <v-btn
                                :disabled="!valid"
                                id="joinBtn"
                                small
                                text
                                type="submit"
                                color="primary"
                            >Join room</v-btn>
                        </template>
                    </v-text-field>
                </v-form>
            </v-col>
            <transition name="fade">
                <v-divider vertical v-if="!inviteUrl"></v-divider>
            </transition>
            <transition name="shrink-x">
                <v-col cols="4" align="center" v-if="!inviteUrl">
                    <v-btn @click="create" text color="primary">Create room</v-btn>
                </v-col>
            </transition>
        </v-row>
        <div class="mine" :style="myBackground">
            <video
                :src-object.prop.camel="localStream"
                autoplay
                muted
                ref="localStream"
                v-if="
                    video &&
                        localStream &&
                        localStream.getVideoTracks().length > 0
                "
            ></video>
        </div>
    </section>
</template>
<script>
import { mapActions, mapMutations, mapGetters } from 'vuex';
import { AvatarGenerator } from 'random-avatar-generator';
export default {
    data() {
        return {
            /* eslint-disable */
            reg: new RegExp('(?:https://.*/room/)?(.*)'),
            /* eslint-enable */
            valid: false,
            inviteUrlRules: [
                url => !!url || 'Invite url is required',
                url => this.reg.test(url) || 'Invite url or room ID  invalid',
            ],
            inviteUrl: null,
            video: true,
            audio: true,
            devices: [],
            videoDevice: null,
            audioDevice: null,
            localStream: null,
            myBackground: '',
        };
    },
    mounted() {
        navigator.mediaDevices.enumerateDevices().then(devices => {
            this.devices = devices;
            this.updateLocalStream();
        });
        this.getBackgroundOfMine();
    },
    computed: {
        ...mapGetters(['account', 'teamName', 'userControl']),
        avatar() {
            const generator = new AvatarGenerator();
            return generator.generateRandomAvatar(
                this.account && this.account.name ? this.account.name : ''
            );
        },
        videoInputDevices() {
            return this.devices.filter(d => d.kind === 'videoinput' && d.label);
        },
        audioInputDevices() {
            return this.devices.filter(d => d.kind === 'audioinput' && d.label);
        },
        audioOutputDevices() {
            return this.devices.filter(
                d => d.kind === 'audiooutput' && d.label
            );
        },
    },
    methods: {
        ...mapMutations(['setLocalStream']),
        ...mapActions([
            'createTeam',
            'join',
            'getVideoStream',
            'getAudioStream',
        ]),
        changeAudioInputTo(audioInputDeviceId) {
            this.audioDevice = audioInputDeviceId;
            this.updateLocalStream();
        },
        changeVideoTo(videoDeviceId) {
            this.videoDevice = videoDeviceId;
            this.updateLocalStream();
        },
        async updateLocalStream() {
            const tracks = [];

            if (this.video) {
                const videoStream = await this.getVideoStream(this.videoDevice);
                tracks.push(videoStream.getVideoTracks()[0]);
            }

            if (this.audio) {
                const audioStream = await this.getAudioStream(this.audioDevice);
                tracks.push(audioStream.getAudioTracks()[0]);
            }

            if (!tracks) {
                this.localStream = null;

                return;
            }
            this.localStream = new MediaStream(tracks);

            this.videoDevice = this.devices.find(
                d => d.label === this.localStream?.getVideoTracks()[0]?.label
            )?.deviceId;
            this.audioDevice = this.devices.find(
                d => d.label === this.localStream?.getAudioTracks()[0]?.label
            )?.deviceId;

            if (!this.localStream) {
                return;
            }
            this.setLocalStream(this.localStream);
        },
        toggleCam() {
            this.video = !this.video;
            this.updateLocalStream();
        },
        toggleMic() {
            this.audio = !this.audio;
            this.updateLocalStream();
        },
        create() {
            this.createTeam();
        },
        joinRoom() {
            if (this.inviteUrl && this.reg.test(this.inviteUrl)) {
                this.updateLocalStream();
                this.$router.push({
                    name: 'room',
                    params: {
                        token: this.inviteUrl
                            .match(this.reg)[1]
                            .substring(0, 15),
                    },
                });
            }
        },
        getBackgroundOfMine() {
            this.$nextTick(() => {
                this.myBackground = `background: url(${this.avatar}); background-position: center; background-repeat: no-repeat; background-color: rgba(0,0,0,0.4);`;
            });
        },
    },
    watch: {
        inviteUrl(val) {
            if (val && this.reg.test(val) && val.length > 15) {
                this.inviteUrl = val.match(this.reg)[1];
            }
        },
        teamName(val) {
            if (val) {
                this.updateLocalStream();
                this.$router.push({ name: 'room', params: { token: val } });
            }
        },
    },
};
</script>
